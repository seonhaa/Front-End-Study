/**
 * Naming Conventions
 *
 * 변수 이름 지을 때
 * 1) 일반적으로 영어를 사용하며 문자와 숫자를 모두 사용 할 수 있다.
 * 2) 특수기호는 언더스코어와 달러를 사용 할 수 있다.
 * 3) 숫자로 이름을 시작 할 수 없다.
 *    1Name, 2Hello
 * 4) 키워드는 변수명으로 사용 할 수 없다.
 *    var const = 'var';
 *
 */
let hwang = "황선하";
var $babymon = "배아비몬스터";
const _ahyeon = "아현";

console.log(hwang);
console.log($babymon);
console.log(_ahyeon);

